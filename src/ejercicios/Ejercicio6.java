package ejercicios;

public class Ejercicio6 {
    /*
    Diseñar un programa que muestre la suma de los 10 primeros números impares.
     */
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i+=2) {   //Inicializando el bucle en 1, lo terminamos en 10 y lo incrementamos en 2
            System.out.println(i);          //Imprimimos por pantalla los numeros impares desde 1 hasta el 10
        }
    }
}
